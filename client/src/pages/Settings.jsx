import { useState, useEffect } from 'react';
import { getIntegrations, getGitHubAuthUrl, getSlackAuthUrl, disconnectIntegration, getCurrentOrg } from '../api';

export default function Settings() {
    const [integrations, setIntegrations] = useState([]);
    const [org, setOrg] = useState(null);
    const [loading, setLoading] = useState(true);

    useEffect(() => {
        loadData();
    }, []);

    const loadData = async () => {
        try {
            const [integrationsRes, orgRes] = await Promise.all([
                getIntegrations(),
                getCurrentOrg().catch(() => ({ data: null }))
            ]);
            setIntegrations(integrationsRes.data);
            setOrg(orgRes.data);
        } catch (err) {
            console.error('Error loading settings:', err);
        } finally {
            setLoading(false);
        }
    };

    const connectGitHub = async () => {
        try {
            const res = await getGitHubAuthUrl();
            window.location.href = res.data.url;
        } catch (err) {
            console.error('Error getting GitHub auth URL:', err);
        }
    };

    const connectSlack = async () => {
        try {
            const res = await getSlackAuthUrl();
            window.location.href = res.data.url;
        } catch (err) {
            console.error('Error getting Slack auth URL:', err);
        }
    };

    const handleDisconnect = async (type) => {
        if (!confirm(`Are you sure you want to disconnect ${type}?`)) return;
        try {
            await disconnectIntegration(type);
            loadData();
        } catch (err) {
            console.error('Error disconnecting:', err);
        }
    };

    const hasGithub = integrations.some(i => i.type === 'github');
    const hasSlack = integrations.some(i => i.type === 'slack');

    if (loading) {
        return <div className="loading">Loading...</div>;
    }

    return (
        <div className="settings">
            <header className="page-header">
                <h1>Settings</h1>
            </header>

            <section className="settings-section">
                <h2>Organization</h2>
                <div className="settings-card">
                    {org ? (
                        <div className="org-info">
                            <div className="org-icon">üè¢</div>
                            <div>
                                <div className="org-name">{org.name}</div>
                                <div className="org-members">{org.members?.length || 1} member(s)</div>
                            </div>
                        </div>
                    ) : (
                        <p>No organization found</p>
                    )}
                </div>
            </section>

            <section className="settings-section">
                <h2>Integrations</h2>

                <div className="integration-card">
                    <div className="integration-info">
                        <div className="integration-icon github">
                            <svg viewBox="0 0 24 24" fill="currentColor">
                                <path d="M12 0C5.37 0 0 5.37 0 12c0 5.31 3.435 9.795 8.205 11.385.6.105.825-.255.825-.57 0-.285-.015-1.23-.015-2.235-3.015.555-3.795-.735-4.035-1.41-.135-.345-.72-1.41-1.23-1.695-.42-.225-1.02-.78-.015-.795.945-.015 1.62.87 1.845 1.23 1.08 1.815 2.805 1.305 3.495.99.105-.78.42-1.305.765-1.605-2.67-.3-5.46-1.335-5.46-5.925 0-1.305.465-2.385 1.23-3.225-.12-.3-.54-1.53.12-3.18 0 0 1.005-.315 3.3 1.23.96-.27 1.98-.405 3-.405s2.04.135 3 .405c2.295-1.56 3.3-1.23 3.3-1.23.66 1.65.24 2.88.12 3.18.765.84 1.23 1.905 1.23 3.225 0 4.605-2.805 5.625-5.475 5.925.435.375.81 1.095.81 2.22 0 1.605-.015 2.895-.015 3.3 0 .315.225.69.825.57A12.02 12.02 0 0024 12c0-6.63-5.37-12-12-12z" />
                            </svg>
                        </div>
                        <div>
                            <div className="integration-name">GitHub</div>
                            <div className="integration-status">
                                {hasGithub ? '‚úì Connected' : 'Not connected'}
                            </div>
                        </div>
                    </div>
                    {hasGithub ? (
                        <button className="btn btn-danger" onClick={() => handleDisconnect('github')}>
                            Disconnect
                        </button>
                    ) : (
                        <button className="btn btn-primary" onClick={connectGitHub}>
                            Connect GitHub
                        </button>
                    )}
                </div>

                <div className="integration-card">
                    <div className="integration-info">
                        <div className="integration-icon slack">
                            <svg viewBox="0 0 24 24" fill="currentColor">
                                <path d="M5.042 15.165a2.528 2.528 0 0 1-2.52 2.523A2.528 2.528 0 0 1 0 15.165a2.527 2.527 0 0 1 2.522-2.52h2.52v2.52zM6.313 15.165a2.527 2.527 0 0 1 2.521-2.52 2.527 2.527 0 0 1 2.521 2.52v6.313A2.528 2.528 0 0 1 8.834 24a2.528 2.528 0 0 1-2.521-2.522v-6.313zM8.834 5.042a2.528 2.528 0 0 1-2.521-2.52A2.528 2.528 0 0 1 8.834 0a2.528 2.528 0 0 1 2.521 2.522v2.52H8.834zM8.834 6.313a2.528 2.528 0 0 1 2.521 2.521 2.528 2.528 0 0 1-2.521 2.521H2.522A2.528 2.528 0 0 1 0 8.834a2.528 2.528 0 0 1 2.522-2.521h6.312zM18.956 8.834a2.528 2.528 0 0 1 2.522-2.521A2.528 2.528 0 0 1 24 8.834a2.528 2.528 0 0 1-2.522 2.521h-2.522V8.834zM17.688 8.834a2.528 2.528 0 0 1-2.523 2.521 2.527 2.527 0 0 1-2.52-2.521V2.522A2.527 2.527 0 0 1 15.165 0a2.528 2.528 0 0 1 2.523 2.522v6.312zM15.165 18.956a2.528 2.528 0 0 1 2.523 2.522A2.528 2.528 0 0 1 15.165 24a2.527 2.527 0 0 1-2.52-2.522v-2.522h2.52zM15.165 17.688a2.527 2.527 0 0 1-2.52-2.523 2.526 2.526 0 0 1 2.52-2.52h6.313A2.527 2.527 0 0 1 24 15.165a2.528 2.528 0 0 1-2.522 2.523h-6.313z" />
                            </svg>
                        </div>
                        <div>
                            <div className="integration-name">Slack</div>
                            <div className="integration-status">
                                {hasSlack ? '‚úì Connected' : 'Not connected'}
                            </div>
                        </div>
                    </div>
                    {hasSlack ? (
                        <button className="btn btn-danger" onClick={() => handleDisconnect('slack')}>
                            Disconnect
                        </button>
                    ) : (
                        <button className="btn btn-primary" onClick={connectSlack}>
                            Connect Slack
                        </button>
                    )}
                </div>
            </section>
        </div>
    );
}
